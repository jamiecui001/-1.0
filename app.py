def 计算起运时间(出生日期, 最近节令日期):
    时间差 = 最近节令日期 - 出生日期
    总秒数 = abs(时间差.total_seconds())
    总天数 = 总秒数 / (24 * 3600)
    岁数 = 总天数 / 3
    岁数整数 = int(岁数)
    剩余天数 = 总天数 - 岁数整数 * 3
    月数 = 剩余天数 * 4
    月数整数 = int(月数)
    剩余天数2 = 剩余天数 - 月数整数 / 4
    剩余小时 = 剩余天数2 * 24
    小时数 = 剩余小时 / 5
    小时整数 = int(小时数)
    剩余小时2 = 剩余小时 - 小时整数 * 5
    分钟数 = 剩余小时2 * 60 / 2
    分钟整数 = int(分钟数)
    起运时间 = 出生日期 + datetime.timedelta(
        days=岁数整数 * 365 + int(月数整数 * 30.44),
        hours=小时整数,
        minutes=分钟整数
    )
    return 岁数, 起运时间


