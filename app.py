def 解析时辰(时输入):
    时输入 = str(时输入).strip()
    match = re.match(r'^(\d+)[:.](\d+)$', 时输入)
    if match:
        小时 = int(match.group(1))
        分钟 = int(match.group(2))
        if 分钟 >= 60:
            小时 += 分钟 // 60
            分钟 = 分钟 % 60
        return 小时, 分钟
    try:
        值 = float(时输入)
        小时 = int(值)
        分钟 = int(round((值 - 小时) * 60))
        if 分钟 >= 60:
            小时 += 分钟 // 60
            分钟 = 分钟 % 60
        return 小时, 分钟
    except:
        pass
    match = re.search(r'(\d+)', 时输入)
    if match:
        小时 = int(match.group(1))
        return 小时, 0
    return 0, 0


