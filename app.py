def 获取日出日落(经度, 纬度, 年, 月, 日):
    try:
        地点 = LocationInfo()
        地点.longitude = 经度
        地点.latitude = 纬度
        地点.timezone = "Asia/Shanghai"
        日期 = datetime.date(年, 月, 日)
        日出 = sun(地点.observer, date=日期, tzinfo=地点.timezone)['sunrise']
        日落 = sun(地点.observer, date=日期, tzinfo=地点.timezone)['sunset']
        return (日出, 日落)
    except:
        return None, None


