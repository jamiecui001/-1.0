def 校正月柱(年, 月, 日, 纬度, 年干):
    偏移天数 = (纬度 - 35) * 2
    出生日期 = datetime.datetime(年, 月, 日)
    精确节气 = 获取当年节气列表(年)
    调整后节令 = []
    for i, 节令 in enumerate(精确节气):
        if i <= 5:
            调整后 = 节令 + datetime.timedelta(days=偏移天数)
        else:
            调整后 = 节令 - datetime.timedelta(days=偏移天数)
        调整后节令.append(调整后)
    出生日期_日期 = 出生日期.date()
    月支 = '子'
    for i in range(len(调整后节令) - 1):
        if 调整后节令[i].date() <= 出生日期_日期 < 调整后节令[i + 1].date():
            月支 = DI_ZHI[(i + 2) % 12]
            break
    else:
        月支 = '丑'
    月干起点 = WU_HU.get(年干, '丙')
    月干索引_base = TIAN_GAN.index(月干起点)
    月支索引 = DI_ZHI.index(月支)
    偏移 = (月支索引 - 2) % 12
    月干 = TIAN_GAN[(月干索引_base + 偏移) % 10]
    校正月 = f"{月干}{月支}"
    return 校正月, 调整后节令


def 校正时柱(经度, 纬度, 年, 月, 日, 时, 分, 年干):
    日出时间, 日落时间 = 获取日出日落(经度, 纬度, 年, 月, 日)
    
    if 日出时间 is None or 日落时间 is None:
        时支 = DI_ZHI[(时 // 2) % 12]
        月干起点 = WU_HU.get(年干, '丙')
        月干索引_base = TIAN_GAN.index(月干起点)
        时支索引 = DI_ZHI.index(时支)
        偏移 = (时支索引 - 2) % 12
        时干 = TIAN_GAN[(月干索引_base + 偏移) % 10]
        return f"{时干}{时支}"
    
    # 移除时区信息（astral 返回带时区的 datetime）
    if hasattr(日出时间, 'tzinfo') and 日出时间.tzinfo is not None:
        日出时间 = 日出时间.replace(tzinfo=None)
    if hasattr(日落时间, 'tzinfo') and 日落时间.tzinfo is not None:
        日落时间 = 日落时间.replace(tzinfo=None)
    
    # 强制统一日期为出生日期（避免跨日比较问题）
    基准日期 = datetime.date(年, 月, 日)
    日出时间 = datetime.datetime(基准日期.year, 基准日期.month, 基准日期.day,
                                  日出时间.hour, 日出时间.minute, 日出时间.second)
    日落时间 = datetime.datetime(基准日期.year, 基准日期.month, 基准日期.day,
                                  日落时间.hour, 日落时间.minute, 日落时间.second)
    
    白天时长 = (日落时间 - 日出时间).total_seconds()
    if 白天时长 <= 0:
        时支 = DI_ZHI[(时 // 2) % 12]
        月干起点 = WU_HU.get(年干, '丙')
        月干索引_base = TIAN_GAN.index(月干起点)
        时支索引 = DI_ZHI.index(时支)
        偏移 = (时支索引 - 2) % 12
        时干 = TIAN_GAN[(月干索引_base + 偏移) % 10]
        return f"{时干}{时支}"
    
    每个时辰时长 = 白天时长 / 6
    时辰区间 = []
    for i in range(6):
        开始 = 日出时间 + datetime.timedelta(seconds=每个时辰时长 * i)
        结束 = 日出时间 + datetime.timedelta(seconds=每个时辰时长 * (i + 1))
        地支 = ['卯', '辰', '巳', '午', '未', '申'][i]
        时辰区间.append({'开始': 开始, '结束': 结束, '地支': 地支})
    
    夜晚时长 = 24 * 3600 - 白天时长
    每个夜晚时辰 = 夜晚时长 / 6
    for i in range(6):
        开始 = 日落时间 + datetime.timedelta(seconds=每个夜晚时辰 * i)
        结束 = 日落时间 + datetime.timedelta(seconds=每个夜晚时辰 * (i + 1))
        地支 = ['酉', '戌', '亥', '子', '丑', '寅'][i]
        时辰区间.append({'开始': 开始, '结束': 结束, '地支': 地支})
    
    输入时间 = datetime.datetime(年, 月, 日, 时, 分, 0)
    
    校正地支 = '子'
    if 输入时间 < 日出时间:
        校正地支 = '寅'
    else:
        for 区间 in 时辰区间:
            if 区间['开始'] <= 输入时间 <= 区间['结束']:
                校正地支 = 区间['地支']
                break
    
    月干起点 = WU_HU.get(年干, '丙')
    月干索引_base = TIAN_GAN.index(月干起点)
    时支索引 = DI_ZHI.index(校正地支)
    偏移 = (时支索引 - 2) % 12
    时干 = TIAN_GAN[(月干索引_base + 偏移) % 10]
    
    return f"{时干}{校正地支}"


